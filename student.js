class Student{
    constructor(id, name, age) {
        this.id = id;
        this.name = name ;
        this. age = age;

    }

    getId(){
        return this.id;
    }
    setId(newId){
        this.id = newId;
    }
    getName(){
        return this.name;
    }
    setId(newName){
        this.name = newName;
    }
    getAge(){
        return this.age;
    }
    setAge(newAge){
        this.age = newAge;
    }


}